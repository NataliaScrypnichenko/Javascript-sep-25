function  filter(arr, callback) {
    let newArr = [];
    for (const item of arr) {
        if (callback(item)){
            newArr[newArr.length] = item;
        }
    }
    return newArr;
}// тут ми описали що буде виконувати ф-я колбек і тепер ми можемо використовувати її до кожного масиву.
